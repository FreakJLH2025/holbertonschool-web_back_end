"""
Module 0-add
Defines a type-annotated function add
"""


def add(a: float, b: float) -> float:
    """
    Returns the sum of a and b as a float.

    Args:
        a (float): first number
        b (float): second number

    Returns:
        float: sum of a and b
    """
    return a + b
